/**
  * @brief  The application entry point.
  * @retval int
  */

FATFS fs;
FIL fnew;
FRESULT res_flash;
UINT fnum;
BYTE ReadBuffer[1024] = {0};       /* 读缓冲区 */
BYTE WriteBuffer[] = "你好啊\r\n"; /* 写缓冲区*/

int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  bsp_debug_usart_init();
  //bsp_led_init();
  // bsp_w25qxx_init();

  res_flash = f_mount(&fs, "1:", 1);

  if (res_flash == FR_NO_FILESYSTEM)
  {
    DEBUG_PRINT("》FLASH还没有文件系统，即将进行格式化...\r\n");

    res_flash = f_mkfs("1:", 0, 0, 10);

    if (res_flash == FR_OK)
    {
      DEBUG_PRINT("》FLASH已成功格式化文件系统。\r\n");
      /* 格式化后，先取消挂载 */
      res_flash = f_mount(NULL, "1:", 1);
      /* 重新挂载	*/
      res_flash = f_mount(&fs, "1:", 1);
    }
    else
    {

      DEBUG_PRINT("format fail\r\n");
      while (1)
        ;
    }
  }
  else if (res_flash != FR_OK)
  {
    DEBUG_PRINT("！！外部Flash挂载文件系统失败。(%d)\r\n", res_flash);
    DEBUG_PRINT("！！可能原因：SPI Flash初始化不成功。\r\n");
    while (1)
      ;
  }
  else
  {
    DEBUG_PRINT("》文件系统挂载成功，可以进行读写测试\r\n");
  }

  bsp_led_pwm_mode_init();
  DEBUG_PRINT("init succeed");

  while (1)
  {
  }
}

